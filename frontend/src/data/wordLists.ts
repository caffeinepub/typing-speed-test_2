export function getWordForLevel(level: number): string {
  if (level <= 2) {
    return easyWords[Math.floor(Math.random() * easyWords.length)];
  } else if (level <= 5) {
    const pool = Math.random() < 0.4 ? easyWords : mediumWords;
    return pool[Math.floor(Math.random() * pool.length)];
  } else if (level <= 8) {
    const pool = Math.random() < 0.3 ? mediumWords : hardWords;
    return pool[Math.floor(Math.random() * pool.length)];
  } else {
    return hardWords[Math.floor(Math.random() * hardWords.length)];
  }
}
